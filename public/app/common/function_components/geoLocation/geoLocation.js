    function _getElevation(areaCode){
        areaCode = areaCode.toString();

        var elevation,
            provinceCode = areaCode.slice(0,2) + '0000',
            cityCode = areaCode.slice(0,4) + '00';

        if (elevationData[provinceCode]) {
            elevation = elevationData[provinceCode][0];
            var provinceData = elevationData[provinceCode][1];

            if (provinceData[cityCode]) {
                elevation = provinceData[cityCode][0];
                var cityData = provinceData[cityCode][1];

                if (cityData[areaCode]) {
                    elevation = cityData[areaCode];
                }
            }
        } else {
            alert('暂无该地区海拔数据');
        }
        return isNaN(elevation)? alert('海拔数据有误') : elevation;
    }

