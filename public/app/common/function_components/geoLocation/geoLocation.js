    function getPositionSuccess(position, callback){
        var lat = position.coords.latitude;     //提取纬度
        var lng = position.coords.longitude;    //提取经度

        var gc = new BMap.Geocoder();
        var point = new BMap.Point(lng, lat);

        consoleLogGeoLocation("getPositionSuccess");

        gc.getLocation(point, function(rs){
            var addComp = rs.addressComponents;
            if(callback && typeof callback == 'function'){
                callback(addComp);
            }
        });
    }

    function getPositionError(error){
        switch(error.code){
            case error.TIMEOUT:
                _DEBUG && alert("连接超时，请重试");
                break;
            case error.PERMISSION_DENIED:
                _DEBUG && alert("您拒绝了使用位置共享服务，查询已取消");
                break;
            case error.POSITION_UNAVAILABLE:
                _DEBUG && alert("暂时无法获取您的位置");
                break;
        }
    }

