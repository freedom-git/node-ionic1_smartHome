    function string2CodeArray(address,data){

        var separator = ',';
        var addrObj = address.split(separator);
        var retCodeArray = [];

        //consoleLogGeoLocation("addrObj[0] = "+addrObj[0]);
        //consoleLogGeoLocation("addrObj[1] = "+addrObj[1]);
        //consoleLogGeoLocation("addrObj[2] = "+addrObj[2]);

        for(var shengI in data)
        {
            var shengArr = data[shengI];
            //consoleLogGeoLocation(shengI+":"+shengArr[0]);

            if(addrObj[0] != shengArr[0])
            {
                continue;
            }
            retCodeArray.push(shengI);

            var shiObj = shengArr[1];
            for(var shiI in shiObj)
            {
                var shiArr = shiObj[shiI];
                //consoleLogGeoLocation("   "+shiI+":"+shiArr[0]);

                if(addrObj[1] != shiArr[0])
                {
                    continue;
                }
                retCodeArray.push(shiI);

                var quObj = shiArr[1];
                for(var quI in quObj)
                {
                    var quArr = quObj[quI];
                    //consoleLogGeoLocation("      "+quI+":"+quArr);

                    if(addrObj[2] == quArr)
                    {
                        //consoleLogGeoLocation("找到了");
                        //consoleLogGeoLocation(address+":"+quI);
                        retCodeArray.push(quI);

                        //consoleLogGeoLocation(retCodeArray);
                        break;
                    }
                }
            }
        }

        return retCodeArray;
    }

